package rc.course_enrollment.service;

public class MathUtils {

    public int add (int a , int b){
        return  a + b ;
    }

   public int computeCircleArea(int radius){

        return  radius * 2;
   }

   public int divide (int num1 , int num2) throws  ArithmeticException{
        return  num1 / num2;
   }
}
